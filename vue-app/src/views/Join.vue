export default class JoinView extends mixins(validationMixin) {
  form: RecipientApplicationData = {
    project: {
      name: '',
      tagline: '',
      description: '',
      category: '',
      problemSpace: '',
    },
    fund: {
      addressName: '',
      resolvedAddress: '',
      plans: '',
    },
    team: {
      name: '',
      description: '',
      email: '',
    },
    links: {
      github: '',
      radicle: '',
      website: '',
      twitter: '',
      discord: '',
    },
    image: {
      bannerHash: '',
      thumbnailHash: '',
    },
    furthestStep: 0,
    hasEns: false,
  }
  currentStep = 0
  steps: string[] = []
  stepNames: string[] = []
  showSummaryPreview = false
  isWaiting = false
  txHash = ''
  txError = ''

  created() {
    const steps = Object.keys(this.form)
    steps.splice(steps.length - 1, 1, 'summary', 'submit')

    const currentStep = steps.indexOf(this.$route.params.step)
    const stepNames = [
      'About the project',
      'Donation details',
      'Team details',
      'Links',
      'Images',
      'Review',
      'Submit',
    ]
    this.steps = steps
    this.currentStep = currentStep
    this.stepNames = stepNames
    this.form = this.$store.state.recipient || this.form

    // redirect to /join/ if step doesn't exist
    if (this.currentStep < 0) {
      this.$router.push({ name: 'join' })
    }
    // "Next" button restricts forward navigation via validation, and
    // eventually updates the `furthestStep` tracker when valid and clicked/tapped.
    // If URL step is ahead of furthest, navigate back to furthest
    if (this.currentStep > this.form.furthestStep) {
      this.$router.push({
        name: 'join-step',
        params: { step: steps[this.form.furthestStep] },
      })
    }
  }

  get isEmailRequired(): boolean {
    return !!process.env.VUE_APP_GOOGLE_SPREADSHEET_ID
  }

  handleToggleTab(event): void {
    const { id } = event.target
    // Guard clause:
    if (
      (!this.showSummaryPreview && id === 'review') ||
      (this.showSummaryPreview && id === 'preview')
    )
      return
    this.showSummaryPreview = !this.showSummaryPreview
  }

  // Check that at least one link is not empty && no links are invalid
  isLinkStepValid(): boolean {
    let isValid = false
    const links = Object.keys(this.form.links)
    for (const link of links) {
      const linkData = this.$v.form.links?.[link]
      if (!linkData) return false
      const isInvalid = linkData.$invalid
      const isEmpty = linkData.$model.length === 0
      if (isInvalid) {
        return false
      } else if (!isEmpty) {
        isValid = true
      }
    }
    return isValid
  }

  isStepValid(step: number): boolean {
    if (this.isWaiting) {
      return !this.isWaiting
    }

    if (step === 3) {
      return this.isLinkStepValid()
    }
    const stepName: string = this.steps[step]
    return !this.$v.form[stepName]?.$invalid
  }

  isStepUnlocked(step: number): boolean {
    return step <= this.form.furthestStep
  }

  saveFormData(updateFurthest?: boolean): void {
    if (updateFurthest && this.currentStep + 1 > this.form.furthestStep) {
      this.form.furthestStep = this.currentStep + 1
    }
    if (typeof this.currentStep !== 'number') return
    this.$store.commit(SET_RECIPIENT_DATA, {
      updatedData: this.form,
      step: this.steps[this.currentStep],
      stepNumber: this.currentStep,
    })
  }

  // Callback from IpfsImageUpload component
  handleUpload(key, value) {
    this.form.image[key] = value
    this.saveFormData(false)
  }

  get isNavDisabled(): boolean {
    return (
      !this.isStepValid(this.currentStep) &&
      this.currentStep !== this.furthestStep
    )
  }

  handleStepNav(step: number, updateFurthest?: boolean): void {
    // If isNavDisabled => disable quick-links
    if (this.isNavDisabled) return
    // Save form data
    this.saveFormData(updateFurthest)
    // Navigate
    if (this.steps[step] === 'submit') {
      this.addRecipient()
    } else {
      if (this.isStepUnlocked(step)) {
        this.$router.push({
          name: 'join-step',
          params: {
            step: this.steps[step],
          },
        })
      }
    }
  }

  get projectInterface(): Project {
    return formToProjectInterface(this.form)
  }

  get furthestStep() {
    return this.form.furthestStep
  }

  get blockExplorer(): { label: string; url: string } {
    return {
      label: chain.explorerLabel,
      url: `${chain.explorer}/address/${this.form.fund.resolvedAddress}`,
    }
  }

  async checkEns(): Promise<void> {
    const { addressName } = this.form?.fund
    if (addressName) {
      const res: string | null = await resolveEns(addressName)
      this.form.hasEns = !!res
      this.form.fund.resolvedAddress = res ? res : addressName
    }
  }

  private async addRecipient() {
    const {
      currentRound,
      currentUser,
      recipient,
      recipientRegistryAddress,
      recipientRegistryInfo,
    } = this.$store.state

    this.isWaiting = true

    // Reset errors when submitting
    this.txError = ''

    if (
      recipientRegistryAddress &&
      recipient &&
      recipientRegistryInfo &&
      currentUser
    ) {
      try {
        if (currentRound && DateTime.now() >= currentRound.votingDeadline) {
          this.$router.push({
            name: 'join',
          })
          throw { message: 'round over' }
        }

        await waitForTransaction(
          addRecipient(
            recipientRegistryAddress,
            recipient,
            recipientRegistryInfo.deposit,
            currentUser.walletProvider.getSigner()
          ),
          (hash) => (this.txHash = hash)
        )

        // Send application data to a Google Spreadsheet
        if (process.env.VUE_APP_GOOGLE_SPREADSHEET_ID) {
          await fetch('/.netlify/functions/recipient', {
            method: 'POST',
            headers: {
              'Content-Type': 'application/json',
            },
            body: JSON.stringify(recipient),
          })
        }

        this.$store.commit(RESET_RECIPIENT_DATA)
      } catch (error) {
        this.isWaiting = false
        this.txError = error.message
        return
      }
      this.isWaiting = false

      this.$router.push({
        name: 'project-added',
        params: {
          hash: this.txHash,
        },
      })
    }
  }
}
</script>

