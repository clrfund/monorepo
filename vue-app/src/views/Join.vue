          validEthAddress: isValidEthAddress,
        },
        resolvedAddress: {},
        plans: { required },
      },
      team: {
        name: {},
        description: {},
        email: {
          email,
          required: process.env.VUE_APP_GOOGLE_SPREADSHEET_ID
            ? required
            : () => true,
        },
      },
      links: {
        github: { url },
        radicle: { url },
        website: { url },
        twitter: { url },
        discord: { url },
      },
      image: {
        bannerHash: {
          required,
          validIpfsHash: isIPFS.cid,
        },
        thumbnailHash: {
          required,
          validIpfsHash: isIPFS.cid,
        },
      },
